    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
                // TODO add your handling code here:
        if(evt.getClickCount() == 2)
        {
            if(Boolean.valueOf(dtm.getValueAt(jTable1.getSelectedRow(), 7).toString()))
            {
            dtm.setValueAt(false, jTable1.getSelectedRow(), 7);
            liquidado = liquidado - lista.get(jTable1.getSelectedRow()).getSaldo();
            lista.get(jTable1.getSelectedRow()).setPagado(0);
            jLabel4.setText(df.format(total));
            jLabel8.setText(df.format(liquidado));
          //  System.out.println(lista.get(jTable1.getSelectedRow()).getId());
            cuentasporpagar.pagar(0,jTextField1.getText(), "id", "=", String.valueOf(lista.get(jTable1.getSelectedRow()).getId())); 
            }
            else
            {
            dtm.setValueAt(true, jTable1.getSelectedRow(), 7);    
            liquidado = liquidado + lista.get(jTable1.getSelectedRow()).getSaldo();
            lista.get(jTable1.getSelectedRow()).setPagado(1);
            jLabel4.setText(df.format(total));
            jLabel8.setText(df.format(liquidado));
           // System.out.println(lista.get(jTable1.getSelectedRow()).getId());
            if(!jTextField1.getText().isEmpty())
            {
            cuentasporpagar.pagar(1,jTextField1.getText(), "id", "=", String.valueOf(lista.get(jTable1.getSelectedRow()).getId())); 
            dtm.setValueAt(jTextField1.getText(), jTable1.getSelectedRow(), 6);    
            }
            else if(lista.get(jTable1.getSelectedRow()).getContraRecibo().equals("0"))
            {
            cuentasporpagar.pagar(1,String.valueOf(lista.get(jTable1.getSelectedRow()).getContraRecibo()), "id", "=", String.valueOf(lista.get(jTable1.getSelectedRow()).getId()));    
            }
            else
            {
            JOptionPane.showMessageDialog(null, "Por favor captura un contrarecibo");        
            dtm.setValueAt(false, jTable1.getSelectedRow(), 7);    
            }
            }
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

