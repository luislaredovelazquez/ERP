    private void jTextField2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyPressed
        // TODO add your handling code here:
          if(evt.getKeyCode() == KeyEvent.VK_BACK_SPACE)
        {
         for (int i = proveedoresTemporales.size()-1; i >= 0; i--) {
         if (proveedoresTemporales.get(i).getNombre().contains(jTextField2.getText())) {
             listaProveedores.add(proveedoresTemporales.get(i));
             proveedoresTemporales.remove(i);           
          }
         }     
        }else
        {
         for (int i = listaProveedores.size()-1; i >= 0; i--) {
         if (!listaProveedores.get(i).getNombre().contains(jTextField2.getText())) {
             proveedoresTemporales.add(listaProveedores.get(i));
             listaProveedores.remove(i);           
          }
         }    
        }
        
         jComboBox1.removeAllItems();
         for (int i = 0; i < listaProveedores.size(); i++) {
         jComboBox1.addItem(listaProveedores.get(i).getNombre());    
         }
    }//GEN-LAST:event_jTextField2KeyPressed

