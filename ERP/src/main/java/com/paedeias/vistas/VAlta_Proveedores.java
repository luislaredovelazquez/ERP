    private void jTextField7FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField7FocusLost
        // TODO add your handling code here:
        if(!jTextField7.getText().isEmpty())
        {
            hProveedores hprove = new hProveedores();
            String texto = jTextField7.getText();
            String arreglo[] = texto.split(" ");
            String sugerencia="";
            int i = 0;
            while(i<arreglo.length && i < 3)
            {
            if(arreglo[i].length()<2)
            sugerencia =  sugerencia+arreglo[i].substring(0,1);
            else
            sugerencia = sugerencia+arreglo[i].substring(0, 2);
            i++;
            }
            int random = hprove.ultimoEntero();
            sugerencia = sugerencia + random;
            jTextField1.setText(sugerencia);
        }

    }//GEN-LAST:event_jTextField7FocusLost


