    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!jTextField1.getText().isEmpty())
        {
        try
        {
        Integer.parseInt(jTextField1.getText());    
        }catch(NumberFormatException e)
        {
        JOptionPane.showMessageDialog(null, "Por favor escriba un número entero en el campo \"CPP-TN\"");    
        return;            
        }
        }
        
        if(!jTextField4.getText().isEmpty() && jRadioButton4.isSelected())
        {
        try
        {
        Integer.parseInt(jTextField4.getText());    
        }catch(NumberFormatException e)
        {
        JOptionPane.showMessageDialog(null, "Por favor escriba un número entero en el campo \"TN\"");    
        return;            
        }
        }
        
        confirmacion = true;
        if(jRadioButton1.isSelected())
            opcion = 0;
        else if(jRadioButton2.isSelected())
            opcion = 1;
        else if(jRadioButton3.isSelected())
        {
            opcion = 2;
            
            if(String.valueOf(jComboBox1.getSelectedItem()).equals("OTRO"))
            {
            banco = jTextField2.getText();    
            }
            else
            banco = String.valueOf(jComboBox1.getSelectedItem());
            
            cheque = jTextField1.getText();
            tn = jTextField3.getText();
        }
        else if(jRadioButton4.isSelected())
        {
            opcion = 3;
            tn = jTextField4.getText();
        }
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

