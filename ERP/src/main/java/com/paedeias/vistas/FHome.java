    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

          if(jTextArea1.getText().isEmpty())
          {
          int respuesta =  JOptionPane.showConfirmDialog(null, "El campo de mensaje está vacío, \n ¿Está seguro de publicar así el texto?", "Confirmar", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
          
          if(Integer.compare(respuesta, JOptionPane.YES_OPTION) != 0)
          {
           return;   
          }
            
          }
        
           blog.setTitulo(jTextField1.getText()); 
           blog.setTexto(jTextArea1.getText());
           blog.setAutor(CConfiguracion.getFoto());
           hblog.guardarBlog(blog);
           listablog.add(0,blog);
           modeloList1.add(0,jTextField1.getText() +" - @"+CConfiguracion.getFoto().toLowerCase());
           list1.setModel(modeloList1);
           jTextField1.setText("");
           jTextArea1.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

