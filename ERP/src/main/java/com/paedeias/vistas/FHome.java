    grantotal = totalventas + totalreservaciones + totalanticipos - totaldevoluciones;    
        
        
    avancetotal = grantotal * 100 / Double.valueOf(CConfiguracion.getMeta());
    avanceventas = totalventas * 100 / Double.valueOf(CConfiguracion.getMeta());
    avanceanticipos = totalanticipos * 100 / Double.valueOf(CConfiguracion.getMeta());
    avancereservaciones = totalreservaciones * 100 / Double.valueOf(CConfiguracion.getMeta());
    avancedevoluciones = totaldevoluciones * 100 / Double.valueOf(CConfiguracion.getMeta());
    
    jSlider1.setValue((int)avancetotal);
    jSlider1.setEnabled(false);
    jSlider1.setToolTipText(df.format(avancetotal) +"%");
    
    jSlider2.setValue((int)avanceventas);
