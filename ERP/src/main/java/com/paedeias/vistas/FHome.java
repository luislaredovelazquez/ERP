    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
            try
        {        
    
        ImpresionMensaje im = new ImpresionMensaje();
        im.inicializar("\n"+jTextField1.getText() + " @"+CConfiguracion.getFoto());
        String contenido = im.generarCadenaImpresion();
        
        if(contenido.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "El mensaje no puede imprimirse debido a: \n 1.- Una palabra contiene más de 16 caracteres");
            return;
        }

        PrinterJob job = PrinterJob.getPrinterJob();
        PageFormat format = new PageFormat();
        Paper p = new Paper();
        p.setSize(CGlobalConfig.getAnchoImp(),CGlobalConfig.getAltoImp());
        p.setImageableArea(CGlobalConfig.getxImp(),CGlobalConfig.getyImp(),CGlobalConfig.getAnchoImp(),CGlobalConfig.getAltoImp());
        format.setPaper(p);
        job.setPageable(new PageableText(new StringReader(contenido), format,18));
        
        if (job.printDialog())
                job.print();
             
              }   catch (IOException ex) {
                Logger.getLogger(FHome.class.getName()).log(Level.SEVERE, null, ex);
            }catch (PrinterException ex) {
                Logger.getLogger(FHome.class.getName()).log(Level.SEVERE, null, ex);
            }
        jTextField1.setText("");        
        JOptionPane.showMessageDialog(null, "Impresión concluida");
    }//GEN-LAST:event_jButton2ActionPerformed

