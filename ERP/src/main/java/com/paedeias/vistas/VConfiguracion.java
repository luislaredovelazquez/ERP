    private void jButton34ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton34ActionPerformed
        // TODO add your handling code here:
        
        
                   try {
                       
            hArticulos harticulos;
            hKardex hkardex;
            hkardex = new hKardex();
            harticulos = new hArticulos();
                       
            if(!CPrincipal.getConexion().crearTransaccion())
            return;
            
            List <Articulos> articulos = harticulos.consultaArticulos("", "*", "");
            List<String> mensajes = new ArrayList<String>();
            for(int i=0; i<articulos.size(); i++)
            {
             Kardex renglon = hkardex.consultaUltimoRenglon("articulo", "=", articulos.get(i).getCodigo());   
             
             if(renglon == null)
             {
              JOptionPane.showMessageDialog(null,"El código "+articulos.get(i).getCodigo()+" no se encuentra en Kardex");   
              return;
             }   
             int totalalmacenes = (int)articulos.get(i).getExistencia()+articulos.get(i).getAlmacenDevoluciones();
             System.out.println(articulos.get(i).getCodigo() + " "+ totalalmacenes + " " + renglon.getExistencias());
             if(Integer.compare(totalalmacenes, renglon.getExistencias())!=0)
             {
             mensajes.add("Las cantidades en el código "+renglon.getArticulo()+" no coinciden");
             }
             if(Integer.compare((int)articulos.get(i).getReservado(), renglon.getReservados())!=0)
             {
             mensajes.add("Las cantidades reservadas en el código "+renglon.getArticulo()+" no coinciden");
             }
             if(Integer.compare(articulos.get(i).getAnticipos(), renglon.getAnticipos())!=0)
             {
             mensajes.add("Las cantidades de anticipos en el código "+renglon.getArticulo()+" no coinciden");
             }
             
            }
            
            FileWriter fichero = null;
            PrintWriter pw = null;
            try
            {
                fichero = new FileWriter("erroresIguales.txt");
                pw = new PrintWriter(fichero);

                for (int i = 0; i < mensajes.size(); i++)
                    pw.println(mensajes.get(i));

            } catch (Exception e) {
                e.printStackTrace();
            } finally {
               try {
               // Nuevamente aprovechamos el finally para 
               // asegurarnos que se cierra el fichero.
               if (null != fichero)
                  fichero.close();
               } catch (Exception e2) {
                  e2.printStackTrace();
               }
            }
            JOptionPane.showMessageDialog(null, "Archivo creado");
            
          CPrincipal.getConexion().finalizarTransaccion();
      
        
        }catch(Exception e)
        {
            CPrincipal.getConexion().cancelarTransaccion();
            e.printStackTrace();            
        }
        
        
    }//GEN-LAST:event_jButton34ActionPerformed

