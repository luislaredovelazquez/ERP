    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        //validar que las dos contraseñas existan y que correspondan a los respectivos puestos
        
        administrador = jTextField1.getText();
        pwdadministrador = String.valueOf(String.valueOf(jPasswordField1.getPassword()));
        
        almacenista = jTextField2.getText();
        pwdalmacenista = String.valueOf(String.valueOf(jPasswordField2.getPassword()));
        
         Autenticar autenticar = new Autenticar();
        
        
        boolean admin = true;
        admin = autenticar.validarAutorizaciones(administrador, pwdadministrador,"Administración");

         if(!admin)
         {
        admin = autenticar.validarAutorizaciones(administrador, pwdadministrador,"Area");
         }
         
         if(!admin)
         {
        admin = autenticar.validarAutorizaciones(administrador, pwdadministrador,"Almacén");
         }
        
         if(!admin)
         {
             JOptionPane.showMessageDialog(null, "Nombre de Usuario o Contraseña del Administrador Inválidos");
             return;
         }
         
         idadministrador = autenticar.getId();
         nombreAdministrador = autenticar.getUsuariogeneral().getNombres()+" "+autenticar.getUsuariogeneral().getApellidoP()+autenticar.getUsuariogeneral().getApellidoM();
         
        boolean almacen = true;  
        almacen = autenticar.validarAutorizaciones(almacenista, pwdalmacenista,"Almacén");
        
         if(!almacen)
         {
             JOptionPane.showMessageDialog(null, "Nombre de Usuario o Contraseña del Almacenista Inválidos");
             return;
         }
         
         idalmacenista = autenticar.getId();
         nombreAlmacenista = autenticar.getUsuariogeneral().getNombres()+" "+autenticar.getUsuariogeneral().getApellidoP()+autenticar.getUsuariogeneral().getApellidoM();
         exito = true;
         dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

