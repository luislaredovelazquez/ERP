    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
                Autenticar autenticador = new Autenticar();
                if (autenticador.validarTransferenciaCompra(jTextField1.getText(), new String(jPasswordField1.getPassword()))) {
                      id = autenticador.getId();
                      autenticar = true;
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(null,
                            "Nombre de Usuario o Contraseña Inválido",
                            "Autenticar",JOptionPane.ERROR_MESSAGE);
                    
                    // reset username and password
                    jTextField1.setText("");
                    jPasswordField1.setText("");
                    autenticar = false;
                }
        
      
        
    }//GEN-LAST:event_jButton1ActionPerformed

