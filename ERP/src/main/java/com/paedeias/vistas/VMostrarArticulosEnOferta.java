    private void inicializar() {
    
        df = new DecimalFormat("0.00");
        vector = new Vector();
        vectorEncabezado = new Vector<String>();
        articulos = new hArticulos();
        
        vectorEncabezado.add("Código");
        vectorEncabezado.add("Descripción");
        vectorEncabezado.add("Precio Venta");
        vectorEncabezado.add("Precio Oferta");
        vectorEncabezado.add("Precio Compra");
        vectorEncabezado.add("Precio Compra + 0.05");
        vectorEncabezado.add("Existencia");
        vectorEncabezado.add("Existencia Almacén Dev.");
        
        listaArticulos = articulos.consultaArticulos("oferta","=","1");
        
         for(Object o: listaArticulos){
             Articulos iarticulos = (Articulos)o;
             Vector<Object> unaFila = new Vector<Object>();
             unaFila.add(iarticulos.getCodigo());
             unaFila.add(iarticulos.getDescripcion());
             unaFila.add(df.format(iarticulos.getPrecioVenta()));
             unaFila.add(df.format(iarticulos.getPrecioVenta2()));
             unaFila.add(df.format(iarticulos.getPrecioCompra()));
             
             double vu = 0;
             vu = iarticulos.getPrecioCompra() + iarticulos.getPrecioCompra() * 0.05;
             
             if(vu > iarticulos.getPrecioVenta2())
             unaFila.add("Inválido");    
             else
             unaFila.add("Válido");        
             
             unaFila.add(iarticulos.getExistencia());
             unaFila.add(iarticulos.getExistenciaAlmacen());
             vector.add(unaFila); 

         }
       
         
       jTable1.getColumnModel().getColumn(0).setPreferredWidth(50); 
       jTable1.getColumnModel().getColumn(1).setPreferredWidth(150); 
       jTable1.getColumnModel().getColumn(2).setPreferredWidth(600);     
        
        dtm = new DefaultTableModel(vector,vectorEncabezado){

              @Override
              public boolean isCellEditable(int row, int column) {
              return false;
                }
              };
        
        
        
        jTable1.setModel(dtm);
        jButton2.setVisible(CConfiguracion.isOfertas());
        jButton1.setVisible(CConfiguracion.isOfertas());
        
    }
