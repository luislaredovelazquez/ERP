    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jTable1.getSelectedRow() == -1)
        {
            JOptionPane.showMessageDialog(null, "Por favor seleccione primero una fila");
            return;
        }
        
        int opcion = JOptionPane.showConfirmDialog(null, "¿Está seguro de que desea quitar este artículo de la lista de ofertas?");
        if(opcion==JOptionPane.YES_OPTION)
        {
        articulos.actualizarOferta(listaArticulos.get(jTable1.getSelectedRow()).getCodigo(), df.format(listaArticulos.get(jTable1.getSelectedRow()).getPrecioVenta2()), 0);
        listaArticulos.remove(jTable1.getSelectedRow());
        dtm.removeRow(jTable1.getSelectedRow());
        dtm.fireTableStructureChanged();
        VRealizarOferta.jButton4.setEnabled(false);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int opcion = JOptionPane.showConfirmDialog(null, "¿Está seguro de que desea quitar todos los artículos de la lista de ofertas?");
        if(opcion==JOptionPane.YES_OPTION)
        {
        articulos.quitarOfertas();   
        listaArticulos.clear();
        vector.clear();
        dtm.fireTableStructureChanged();
        VRealizarOferta.jButton4.setEnabled(false);
        JOptionPane.showMessageDialog(null, "Artículos eliminados de Ofertas");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

