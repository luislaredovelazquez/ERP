    private void inicializar() {
    hreimpresiones =new hReimpresiones();
    partidas = new ArrayList<Reimpresiones>();

    
    encabezado = new Vector<String>();
    
    encabezado.add("Usuario");
    encabezado.add("Folio");
    encabezado.add("Fecha");
    encabezado.add("Importe");
    encabezado.add("Vendedor");


    partidas = hreimpresiones.consultaPedidos("", "*", "");

    
    
    vector = new Vector();
    
    dtm = new DefaultTableModel(vector,encabezado){

              @Override
              public boolean isCellEditable(int row, int column) {
              return false;
                }
              };
    
             for(Object o: partidas){
             Reimpresiones iarticulos = (Reimpresiones)o;
             Vector<Object> unaFila = new Vector<Object>();
             unaFila.add(iarticulos.getUsuario());
             unaFila.add(iarticulos.getFolio());
             unaFila.add(iarticulos.getFecha());
             unaFila.add(iarticulos.getImporte());
             unaFila.add(iarticulos.getVendedor());
             vector.add(unaFila);
             }
             
           
             
    jTable1.setModel(dtm);
    dtm.fireTableDataChanged();
    }
    
    
