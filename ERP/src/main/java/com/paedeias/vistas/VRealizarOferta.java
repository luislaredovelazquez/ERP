    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try
        {
         double vu = 0;
         for(int i=0; i<listaArticulosP.size(); i++)
        {
            vu = listaArticulosP.get(i).getPrecioCompra() + (listaArticulosP.get(i).getPrecioCompra() * CGlobalConfig.getUtilidad());
            vu = vu + (vu * CGlobalConfig.getIvaVenta());
            if(vu > listaArticulosP.get(i).getPrecioVenta2()) 
            {
                JOptionPane.showMessageDialog(null, "Existe un artículo con un precio de venta menor al precio de compra en la lista");
                return;
            }
        }
            if(!CPrincipal.getConexion().crearTransaccion())
            return;
        
        for(int i=0; i<listaArticulosP.size(); i++)
        {
            articulos.actualizarOferta(listaArticulosP.get(i).getCodigo(),df.format(listaArticulosP.get(i).getPrecioVenta2()) , listaArticulosP.get(i).getOferta());
        }
        
        jButton4.setEnabled(false);
        JOptionPane.showMessageDialog(null,"Oferta creada");
           CPrincipal.getConexion().finalizarTransaccion();
         
        }catch(Exception e)
        {
            CPrincipal.getConexion().cancelarTransaccion();
            e.printStackTrace();            
        }
    }//GEN-LAST:event_jButton4ActionPerformed

