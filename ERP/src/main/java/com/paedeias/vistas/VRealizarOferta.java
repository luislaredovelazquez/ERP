    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
            if(jTable1.getSelectedRow() == -1)
        {
            JOptionPane.showMessageDialog(null, "Por favor seleccione primero una fila");
            return;
        }
        
        if(listaArticulos.get(jTable1.getSelectedRow()).getOferta()==1)
        {
            JOptionPane.showMessageDialog(null, "Este artículo ya se está vendiendo con un precio especial");
            return;
        }
        
        if(!Validadores.validarEntero("Utilidad/Descuento", jTextField4.getText()))
        {
        return;
        }
        
        if(!listaArticulosP.isEmpty())
        {
        for(int a=0; a<listaArticulosP.size(); a++)
        {
            if(listaArticulosP.get(a).getCodigo().equals(listaArticulos.get(jTable1.getSelectedRow()).getCodigo()))
            {
         JOptionPane.showMessageDialog(null, "Este artículo ya se encuentra en la lista actual");   
         return;                
            }
        }
        }
        
        Articulos articulo = new Articulos();
        articulo.setCodigo(listaArticulos.get(jTable1.getSelectedRow()).getCodigo());
        articulo.setDescripcion(listaArticulos.get(jTable1.getSelectedRow()).getDescripcion());
        articulo.setExistencia(listaArticulos.get(jTable1.getSelectedRow()).getExistencia());
        articulo.setExistenciaAlmacen(listaArticulos.get(jTable1.getSelectedRow()).getExistenciaAlmacen());
        articulo.setPrecioVenta(listaArticulos.get(jTable1.getSelectedRow()).getPrecioVenta());
        articulo.setPrecioCompra(listaArticulos.get(jTable1.getSelectedRow()).getPrecioCompra());
        double precioVenta2 = 0;
        if(jCheckBox2.isSelected())
        precioVenta2 = listaArticulos.get(jTable1.getSelectedRow()).getPrecioVenta() - (listaArticulos.get(jTable1.getSelectedRow()).getPrecioVenta() * (double)(Integer.valueOf(jTextField4.getText()))/100);
        else
        {
        precioVenta2 = listaArticulos.get(jTable1.getSelectedRow()).getPrecioCompra() + (listaArticulos.get(jTable1.getSelectedRow()).getPrecioCompra() * (double)(Integer.valueOf(jTextField4.getText()))/100);
        precioVenta2 = precioVenta2 * (1+CGlobalConfig.getIvaVenta());
        }
        
        
        articulo.setPrecioVenta2(precioVenta2);
        articulo.setOferta(1);
        
        listaArticulosP.add(articulo);
        
             Vector<Object> unaFila = new Vector<Object>();
             unaFila.add(articulo.getCodigo());
             unaFila.add(articulo.getDescripcion());
             unaFila.add(df.format(articulo.getPrecioVenta()));
             unaFila.add(df.format(articulo.getPrecioVenta2()));
             unaFila.add(df.format(articulo.getPrecioCompra()));
             
             double vu = 0;
             vu = articulo.getPrecioCompra() + articulo.getPrecioCompra() * CGlobalConfig.getUtilidad();
             
             if(vu > articulo.getPrecioVenta2())
             unaFila.add("Inválido");    
             else
             unaFila.add("Válido");        
             
             unaFila.add(articulo.getExistencia());
             unaFila.add(articulo.getExistenciaAlmacen());
             vectorPartidas.add(unaFila); 


             
              dtmPartidas.fireTableStructureChanged();

//              jTable2.setModel(dtm2);
              jTable2.getSelectionModel().setSelectionInterval(0, 0);
    }//GEN-LAST:event_jButton1ActionPerformed

