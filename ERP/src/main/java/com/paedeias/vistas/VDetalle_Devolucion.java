    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
                 // TODO add your handling code here:
         if( jCheckBox1.isSelected())
         {
             JOptionPane.showMessageDialog(null, "Esta venta ya ha sido facturada, por favor consulta el catálogo de facturas");
             return;
         } 
        
        double iva = devolucion.getTotal() * 0.16;
        double subtotal = devolucion.getTotal()-iva;
        
        Numero_a_Letra numeroaletra = new Numero_a_Letra();
        Factura ventafactura = new Factura();
        
        ventafactura.setFormaDePago(devolucion.getTipoDeVenta().toLowerCase());
        ventafactura.setSubtotal(String.valueOf(subtotal));
        ventafactura.setImpuesto(String.valueOf(iva));
        ventafactura.setTotal(String.valueOf(devolucion.getTotal()));
        ventafactura.setIdConceptoFactura("DV"+devolucion.getId());
        ventafactura.setTotalLetra(numeroaletra.Convertir(String.valueOf(devolucion.getTotal()), true));
        ventafactura.setIdReceptorF(devolucion.getIdcliente().intValue());
        ventafactura.setMovimiento("Devolución Venta "+devolucion.getTipoDeVenta());
        ventafactura.setNumMovimiento(String.valueOf(devolucion.getId()));
        
        hFacturasCFDI hfacturas = new hFacturasCFDI();
        Factura factemporal=hfacturas.consultaUltimaFactura("idConceptoFactura", "=", devolucion.getCodigoVenta());
        if(factemporal != null && factemporal.getFolio() != null)
        ventafactura.setMotivo("Devolución de la factura "+factemporal.getFolio()+" con fecha "+factemporal.getFecha().substring(0, 19));
        else
        ventafactura.setMotivo("Devolución de la venta "+devolucion.getCodigoVenta()+" con fecha "+devolucion.getFechaVenta().substring(0, 19));    
        
        
        VDetalle_NC_CFDI factura = new VDetalle_NC_CFDI(ventafactura,"nc");
        int indice = ((JTabbedPane) getParent().getParent().getParent()).getSelectedIndex();
        indice += 1;
        ((JTabbedPane) getParent().getParent().getParent()).add(new JScrollPane(factura), indice);
        ((JTabbedPane) getParent().getParent().getParent()).setTabComponentAt(indice, new botonCierre("Nota de Crédito", indice));
        ((JTabbedPane) getParent().getParent().getParent()).setSelectedIndex(indice);
    }//GEN-LAST:event_jButton4ActionPerformed

