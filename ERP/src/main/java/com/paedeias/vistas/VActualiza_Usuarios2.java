    public class PSeleccionar_Permisos extends JPanel   {

    JPanel panelPrincipal, panelEncabezado, panelTabla;
    JTable tablaPermisos;
    Vector vector,encabezadoPermisos;
    DefaultTableModel dtm;
   List<Proveedores> listaPermisos;

   public PSeleccionar_Permisos(){
     tablaPermisos = new JTable();
     encabezadoPermisos = new Vector<String>();
     encabezadoPermisos.add("Permiso");
     encabezadoPermisos.add("Bloquear");


     vector = new Vector();

       for(int i=0; i<CGlobalConfig.getTitulos_permisos().length; i++)
       {
             Vector<Object> unaFila = new Vector<Object>();
             unaFila.add(CGlobalConfig.getTitulos_permisos()[i]);
             if(arreglo_permiso[i].equals("1"))
             unaFila.add(true);
             else
             unaFila.add(false);    
             vector.add(unaFila);
         }

      dtm = new DefaultTableModel(vector,encabezadoPermisos) {

              @Override
              public boolean isCellEditable(int row, int column) {
              return false;
                }
              
               @Override
              public Class<?> getColumnClass(int columnIndex) {
              if (columnIndex == 1) {
              return Boolean.class;
               }
               return super.getColumnClass(columnIndex);
               }
              
              
              };

      tablaPermisos.setModel(dtm);
      tablaPermisos.addMouseListener(new MouseListener()
      {

                public void mouseClicked(MouseEvent e) {
       if(e.getClickCount() == 2)
        {
            if(Boolean.valueOf(dtm.getValueAt(tablaPermisos.getSelectedRow(), 1).toString()))
            {
            dtm.setValueAt(false, tablaPermisos.getSelectedRow(), 1);
            arreglo_permiso[tablaPermisos.getSelectedRow()] = "0";
            }
            else
            {
            dtm.setValueAt(true, tablaPermisos.getSelectedRow(), 1);    
            arreglo_permiso[tablaPermisos.getSelectedRow()] = "1";
            }
        }
                }

                public void mousePressed(MouseEvent e) {
                    return;
                }

                public void mouseReleased(MouseEvent e) {
                   return;
                }

                public void mouseEntered(MouseEvent e) {
                    return;
                }

                public void mouseExited(MouseEvent e) {
                   return;
                }

      }
              );

   add(new JScrollPane(tablaPermisos));
      setLayout(new GridLayout());
    }

}
    
