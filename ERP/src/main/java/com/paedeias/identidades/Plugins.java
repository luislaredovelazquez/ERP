package com.paedeias.identidades;

/**
 *
 * @author ALL
 */
public class Plugins {

    Long id;
    String idPlugin="";
    String permisosPlugin="";
    
    public Plugins() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getIdPlugin() {
        return idPlugin;
    }

    public void setIdPlugin(String idPlugin) {
        this.idPlugin = idPlugin;
    }

    public String getPermisosPlugin() {
        return permisosPlugin;
    }

    public void setPermisosPlugin(String permisosPlugin) {
        this.permisosPlugin = permisosPlugin;
    }
    
    
    
}
